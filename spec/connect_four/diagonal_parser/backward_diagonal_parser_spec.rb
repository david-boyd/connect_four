RSpec.describe BackwardDiagonalParser do

  it 'verify diagonals columns correctly retrieved' do
    grid = [['36', '29', '22', '15', '8', '1'],
            ['37', '30', '23', '16', '9', '2'],
            ['38', '31', '24', '17', '10', '3'],
            ['39', '32', '25', '18', '11', '4'],
            ['40', '33', '26', '19', '12', '5'],
            ['41', '34', '27', '20', '13', '6'],
            ['42', '35', '28', '21', '14', '7']]
    # assuming board looks like
    # [36] [37] [38] [39] [40] [41] [42]
    # [29] [30] [31] [32] [33] [34] [35]
    # [22] [23] [24] [25] [26] [27] [28]
    # [15] [16] [17] [18] [19] [20] [21]
    # [8]  [9]  [10] [11] [12] [13] [14]
    # [1]  [2]  [3]  [4]  [5]  [6]  [7]
    expect(BackwardDiagonalParser.new(grid).parse(4)).to eq([
                                                                ["4", "10", "16", "22"],
                                                                ["5", "11", "17", "23", "29"],
                                                                ["6", "12", "18", "24", "30", "36"],
                                                                ["7", "13", "19", "25", "31", "37"],
                                                                ["14", "20", "26", "32", "38"],
                                                                ["21", "27", "33", "39"]])
  end


end