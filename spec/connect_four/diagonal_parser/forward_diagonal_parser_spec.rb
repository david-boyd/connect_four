RSpec.describe ForwardDiagonalParser do

  it 'verify diagonals columns correctly retrieved' do
    grid = [['36', '29', '22', '15', '8', '1'],
            ['37', '30', '23', '16', '9', '2'],
            ['38', '31', '24', '17', '10', '3'],
            ['39', '32', '25', '18', '11', '4'],
            ['40', '33', '26', '19', '12', '5'],
            ['41', '34', '27', '20', '13', '6'],
            ['42', '35', '28', '21', '14', '7']]
    # assuming board looks like
    # [36] [37] [38] [39] [40] [41] [42]
    # [29] [30] [31] [32] [33] [34] [35]
    # [22] [23] [24] [25] [26] [27] [28]
    # [15] [16] [17] [18] [19] [20] [21]
    # [8]  [9]  [10] [11] [12] [13] [14]
    # [1]  [2]  [3]  [4]  [5]  [6]  [7]
    expect(ForwardDiagonalParser.new(grid).parse(4)).to eq([
                                                               ["4", "12", "20", "28"],
                                                               ["3", "11", "19", "27", "35"],
                                                               ["2", "10", "18", "26", "34", "42"],
                                                               ["1", "9", "17", "25", "33", "41"],
                                                               ["8", "16", "24", "32", "40"],
                                                               ["15", "23", "31", "39"]])
  end
end